countriesData.sort((a, b) => a.name.localeCompare(b.name))

export function getCountryByCode(code: string): Country | undefined {
  return countriesData.find((country) => country.code === code)
}

export function getStatesByCountryCode(countryCode: string): State[] {
  const country = getCountryByCode(countryCode)
  return country?.states || []
}

export function getCountryName(code: string): string {
  const country = getCountryByCode(code)
  return country?.name || code
}

export function getStateName(countryCode: string, stateCode: string): string {
  const states = getStatesByCountryCode(countryCode)
  const state = states.find((s) => s.code === stateCode)
  return state?.name || stateCode
}
